export function Sidebar() {
  const pathname = usePathname();
  const { isAuthenticated, user, logout } = useAuth();

  if (!isAuthenticated) return null;

  const pathSegments = pathname.split("/");
  const activePath = "/" + (pathSegments[2] || "");

  return (
    <aside className="flex h-screen w-52 shrink-0 flex-col border-r bg-muted/30">
      {/* Logo */}
      <div className="flex items-center gap-2 px-4 py-3 border-b">
        <Link href="/" className="flex items-center gap-2">
          <span className="text-lg font-bold text-primary">FEXORA</span>
          <span className="text-[10px] rounded bg-primary/10 px-1.5 py-0.5 font-semibold text-primary">ACP</span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 overflow-y-auto py-1">
        {NAV_SECTIONS.map((section) => (
          <div key={section.title}>
            <p className="px-4 pt-3 pb-1 text-[10px] font-semibold uppercase tracking-wider text-muted-foreground/60">
              {section.title}
            </p>
            {section.items.map((item) => {
              const isActive =
                item.href === "/"
                  ? activePath === "/" || activePath === ""
                  : activePath.startsWith(item.href);
              return (
                <Link key={item.href} href={item.href}>
                  <div
                    className={cn(
                      "mx-2 flex items-center gap-2.5 rounded-md px-2.5 py-1.5 text-[13px] transition-colors",
                      isActive
                        ? "bg-primary text-primary-foreground"
                        : "text-muted-foreground hover:bg-muted hover:text-foreground"
                    )}
                  >
                    {item.icon}
                    <span>{item.label}</span>
                  </div>
                </Link>
              );
            })}
          </div>
        ))}
      </nav>

      {/* Footer */}
      <div className="border-t p-3 space-y-2">
        <div className="flex items-center justify-between">
          <div className="min-w-0">
            <p className="text-xs font-medium truncate">
              {user?.profile?.username ?? user?.email}
            </p>
            <p className="text-[10px] text-muted-foreground">{user?.role}</p>
          </div>
          <ThemeToggle />
        </div>
        <Button
          variant="ghost"
          size="sm"
          className="w-full text-xs"
          onClick={logout}
        >
          Sign Out
        </Button>
      </div>
    </aside>
  );
}
